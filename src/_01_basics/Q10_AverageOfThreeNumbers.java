package _01_basics;

// Q10: Write a program to find the average of three numbers
public class Q10_AverageOfThreeNumbers {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = 30;
        double average = (a + b + c) / 3.0;
        System.out.println("Average: " + average);
    }
}
