package _02_if_else;

// Q5: Check if a person is eligible to vote (age >= 18)
public class Q05_VotingEligibility {
    public static void main(String[] args) {
        int age = 20;
        if (age >= 18) {
            System.out.println("Eligible to vote");
        } else {
            System.out.println("Not eligible to vote");
        }
    }
}
