package _02_if_else;

// Q3: Find the largest of two numbers
public class Q03_LargestOfTwo {
    public static void main(String[] args) {
        int a = 15;
        int b = 20;
        if (a > b) {
            System.out.println(a + " is greater");
        } else {
            System.out.println(b + " is greater");
        }
    }
}
