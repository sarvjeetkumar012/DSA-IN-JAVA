package _04_pattern_printing;

// Q4: Print a square pattern with numbers
public class Q04_NumberSquare {
    public static void main(String[] args) {
        int n = 4;
        for (int i = 1; i <= n; i++) {
            for (int j = 1; j <= n; j++) {
                System.out.print(j + " ");
            }
            System.out.println();
        }
    }
}
